package com.pkrm.cric.model;

public class Blower {

	private String name;
	private int madiens;
	private float overs;
	private int runs;
	private int wicket;
	private int noBall;
	private int wide;
	private float economy;

	public final String getName() {
		return name;
	}

	public final void setName(String name) {
		this.name = name;
	}

	public final int getMadiens() {
		return madiens;
	}

	public final void setMadiens(int madiens) {
		this.madiens = madiens;
	}

	public final float getOvers() {
		return overs;
	}

	public final void setOvers(float overs) {
		this.overs = overs;
	}

	public final int getRuns() {
		return runs;
	}

	public final void setRuns(int runs) {
		this.runs = runs;
	}

	public final int getWicket() {
		return wicket;
	}

	public final void setWicket(int wicket) {
		this.wicket = wicket;
	}

	public final int getNoBall() {
		return noBall;
	}

	public final void setNoBall(int noBall) {
		this.noBall = noBall;
	}

	public final int getWide() {
		return wide;
	}

	public final void setWide(int wide) {
		this.wide = wide;
	}

	public final float getEconomy() {
		return economy;
	}

	public final void setEconomy(float economy) {
		this.economy = economy;
	}

}
